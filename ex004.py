algo = input('\033[1;34mDigite algo: \033[0;35m')
print('O tipo primitivo desse valor é: \033[36m', type(algo))
print('\033[33mSó tem espaços?\033[31m', algo.isspace())
print('\033[35mÉ um número?\033[36m', algo.isnumeric())
print('\033[33mÉ alfabético?\033[31m', algo.isalpha())
print('\033[35mÉ alfanumérico?\033[36m', algo.isalnum())
print('\033[33mEstá em maiúsculas?\033[31m', algo.isupper())
print('\033[35mEstá em minúsculas?\033[36m', algo.islower())
print('\033[33mEstá capitalizada?\033[31m', algo.istitle())
